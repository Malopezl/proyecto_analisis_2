    private void botonEliminarPuestoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonEliminarPuestoActionPerformed
        // TODO add your handling code here:
       if(this.jTablePuestos.getSelectedRow()>=0){
        String[] botones = {"Eliminar Puesto", " Cancelar"};
        int eleccion = JOptionPane.showOptionDialog(null, " ¿Esta seguro que desea eliminar este puesto?. Esta acción será permanente y no podrá recuperar los datos.", "Eliminar puesto", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE, null/*icono*/, botones, botones[0]);
        if (eleccion == 0) {
            Puesto puesto = this.puestos.get(this.jTablePuestos.getSelectedRow());
            ControladorPuesto.ElminarPuesto(puesto);
            JOptionPane.showMessageDialog(this, "El puesto fue eliminado exitosamente.", "Puesto eliminado exitosamente", JOptionPane.INFORMATION_MESSAGE);
            this.obtenerPuestos();
        }
       }else{
           JOptionPane.showMessageDialog(this, "Seleccione un puesto de la lista para eliminar.", "Selección de puesto incorrecta", JOptionPane.ERROR_MESSAGE);
       }
    }//GEN-LAST:event_botonEliminarPuestoActionPerformed

    private void botonEditarPuestoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonEditarPuestoActionPerformed
        // TODO add your handling code here:
        if(this.jTablePuestos.getSelectedRow()>=0){
        Puesto puesto=this.puestos.get(this.jTablePuestos.getSelectedRow());
        EditarPuesto editar;
        editar = new EditarPuesto(puesto);
        editar.setVisible(true);
        }else{
            JOptionPane.showMessageDialog(this, "Seleccione un menú de la lista para editar.", "Selección de menú incorrecta", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_botonEditarPuestoActionPerformed

