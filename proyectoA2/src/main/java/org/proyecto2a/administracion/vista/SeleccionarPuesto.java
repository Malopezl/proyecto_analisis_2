      private void obtenerPuestos(){
        this.puestos = PuestoSql.getAllPuestos();
        DefaultTableModel tabla = (DefaultTableModel) jTablePuestos.getModel();
        tabla.setRowCount(0);
        for(int i=0; i<this.puestos.size(); i++)
        {
           tabla.addRow(new Object[]{puestos.get(i).getId(), puestos.get(i).getNombre()});  
        }
    }

