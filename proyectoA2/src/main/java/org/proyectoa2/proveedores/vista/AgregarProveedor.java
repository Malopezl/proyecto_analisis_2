    private void botonAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAceptarActionPerformed
        if (campoNit.getText().equals("") || campoNombre.getText().equals("") || campoCuenta.getText().equals("") || campoTelefono.getText().equals("") 
                  || campoDireccion.getText().equals("") || campoCorreo.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Revise que todos los campos han sido llenados correctamente", "Error", JOptionPane.WARNING_MESSAGE);
        } else {
            if (JOptionPane.showConfirmDialog(null, "Los datos son correctos?", "Confirmar", JOptionPane.YES_NO_OPTION) == 0) {
                proveedor.setNit(campoNit.getText());
                proveedor.setNombreProveedor(campoNombre.getText());
                proveedor.setNoCuenta(campoCuenta.getText());
                proveedor.setTelefono(campoTelefono.getText());
                proveedor.setDireccionProveedor(campoDireccion.getText());
                proveedor.setCorreo(campoCorreo.getText());
                proveedor.setSaldoPendiente(0);
                manejoProveedor.registrarProveedor(proveedor);
                JOptionPane.showMessageDialog(null, "Se ha agregado el proveedor con exito", "Nuevo Proveedor", JOptionPane.INFORMATION_MESSAGE);
                limpiar();
            }
            
        }
    }//GEN-LAST:event_botonAceptarActionPerformed

